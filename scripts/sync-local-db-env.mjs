import fs from "node:fs"
import { config as loadEnv } from "dotenv"
import {
  defaultLocalSchemaName,
  envLocalPath,
  envPath,
  sanitizeSchemaName,
  upsertEnvValue,
  withSchema,
} from "./local-env-utils.mjs"

loadEnv({ path: envPath })
loadEnv({ path: envLocalPath, override: true })

const baseDatabaseUrl = process.env.DATABASE_URL
const baseDirectUrl = process.env.DIRECT_URL

if (!baseDatabaseUrl || !baseDirectUrl) {
  throw new Error(
    "No pude encontrar DATABASE_URL y DIRECT_URL base en .env. Configura .env primero y luego corre npm run db:local:setup."
  )
}

const requestedSchema =
  process.env.DEV_DB_SCHEMA || defaultLocalSchemaName()
const schemaName = sanitizeSchemaName(requestedSchema)
const localDatabaseUrl = withSchema(baseDatabaseUrl, schemaName)
const localDirectUrl = withSchema(baseDirectUrl, schemaName)

if (!fs.existsSync(envLocalPath)) {
  fs.writeFileSync(
    envLocalPath,
    "# Local-only overrides generated by npm run db:local:setup\n",
    "utf8"
  )
}

upsertEnvValue(envLocalPath, "DEV_DB_SCHEMA", schemaName)
upsertEnvValue(envLocalPath, "DATABASE_URL", localDatabaseUrl)
upsertEnvValue(envLocalPath, "DIRECT_URL", localDirectUrl)
upsertEnvValue(envLocalPath, "NEXTAUTH_URL", "http://localhost:3000")
upsertEnvValue(envLocalPath, "NEXT_PUBLIC_APP_URL", "http://localhost:3000")

console.log(
  [
    "Configuracion local segura lista.",
    `Schema local: ${schemaName}`,
    "DATABASE_URL y DIRECT_URL en .env.local ahora apuntan a una schema separada del mismo Postgres.",
    "Siguiente paso: npm run db:local:setup ya continuara con db push y seed sobre esa schema.",
  ].join("\n")
)
